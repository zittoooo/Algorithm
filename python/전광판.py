bulb = {'0':'1110111',
        '1':'0100100',
        '2':'1011101',
        '3':'1101101',
        '4':'0101110',
        '5':'1101011',
        '6':'1111011',
        '7':'0100111',
        '8':'1111111',
        '9':'1101111',
        ' ':'0000000'}
        
t = int(input())
while t:
    t -= 1
    a, b = input().split()
    
    a = ' ' * (5 - len(a)) + a
    b = ' ' * (5 - len(b)) + b
    
    res = 0
    for i in range(5):
        for j in range(7):
            res += (bulb[a[i]][j] != bulb[b[i]][j])
            
    print(res)
